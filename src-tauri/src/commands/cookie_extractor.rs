fn get_firefox_cookie_path() -> Option<PathBuf> {
    #[cfg(target_os = "macos")]
    {
        if let Ok(home) = std::env::var("HOME") {
            let profiles_path =
                PathBuf::from(home).join("Library/Application Support/Firefox/Profiles");
            if profiles_path.exists() {
                if let Ok(entries) = std::fs::read_dir(&profiles_path) {
                    for entry in entries.flatten() {
                        let cookie_path = entry.path().join("cookies.sqlite");
                        if cookie_path.exists() {
                            return Some(cookie_path);
                        }
                    }
                }
            }
        }
    }

    #[cfg(target_os = "linux")]
    {
        if let Ok(home) = std::env::var("HOME") {
            let profiles_path = PathBuf::from(home).join(".mozilla/firefox");
            if profiles_path.exists() {
                if let Ok(entries) = std::fs::read_dir(&profiles_path) {
                    for entry in entries.flatten() {
                        let cookie_path = entry.path().join("cookies.sqlite");
                        if cookie_path.exists() {
                            return Some(cookie_path);
                        }
                    }
                }
            }
        }
    }

    #[cfg(target_os = "windows")]
    {
        if let Ok(appdata) = std::env::var("APPDATA") {
            let profiles_path = PathBuf::from(appdata).join("Mozilla\\Firefox\\Profiles");
            if profiles_path.exists() {
                if let Ok(entries) = std::fs::read_dir(&profiles_path) {
                    for entry in entries.flatten() {
                        let cookie_path = entry.path().join("cookies.sqlite");
                        if cookie_path.exists() {
                            return Some(cookie_path);
                        }
                    }
                }
            }
        }
    }

    None
}

// ==================== Cookie 读取和解密 ====================

fn count_cookies(path: &PathBuf) -> Result<usize, String> {
    let conn = Connection::open(path).map_err(|e| format!("Failed to open database: {}", e))?;

    let table = infer_cookie_table(&conn)?;
    let query = format!("SELECT COUNT(*) FROM {}", table);

    let count: i64 = conn
        .query_row(&query, [], |row| row.get(0))
        .map_err(|e| format!("Query failed: {}", e))?;

    Ok(count as usize)
}

fn infer_cookie_table(conn: &Connection) -> Result<&'static str, String> {
    let result: Result<String, _> = conn.query_row(
        "SELECT name FROM sqlite_master WHERE type='table' AND name='moz_cookies'",
        [],
        |row| row.get(0),
    );
    if result.is_ok() {
        return Ok("moz_cookies");
    }
    Ok("cookies")
}

fn map_chrome_cookie_row(row: &rusqlite::Row) -> rusqlite::Result<CookieEntry> {
    let encrypted_value: Vec<u8> = row.get(1)?;
    Ok(CookieEntry {
        name: row.get(0)?,
        value: decrypt_cookie_value(&encrypted_value),
        domain: row.get(2)?,
        path: row.get(3)?,
        expires: row.get(4)?,
        secure: row.get(5)?,
        httponly: row.get(6)?,
    })
}

fn map_firefox_cookie_row(row: &rusqlite::Row) -> rusqlite::Result<CookieEntry> {
    let value: String = row.get(1)?;
    Ok(CookieEntry {
        name: row.get(0)?,
        value,
        domain: row.get(2)?,
        path: row.get(3)?,
        expires: row.get(4)?,
        secure: row.get(5)?,
        httponly: row.get(6)?,
    })
}

fn read_cookies(path: &PathBuf, domain_filter: Option<String>) -> Result<Vec<CookieEntry>, String> {
    let conn = Connection::open(path).map_err(|e| format!("Failed to open database: {}", e))?;
    let table = infer_cookie_table(&conn)?;

    let (query, param) = if table == "moz_cookies" {
        let base = "SELECT name, value, host, path, expiry, isSecure, isHttpOnly FROM moz_cookies";
        match domain_filter {
            Some(ref domain) => (
                format!("{} WHERE host LIKE ?1", base),
                Some(format!("%{}%", domain)),
            ),
            None => (base.to_string(), None),
        }
    } else {
        let base = "SELECT name, encrypted_value, host_key, path, expires_utc, is_secure, is_httponly FROM cookies";
        match domain_filter {
            Some(ref domain) => (
                format!("{} WHERE host_key LIKE ?1", base),
                Some(format!("%{}%", domain)),
            ),
            None => (base.to_string(), None),
        }
    };

    let mut stmt = conn
        .prepare(&query)
        .map_err(|e| format!("Prepare failed: {}", e))?;

    let mut cookies = Vec::new();

    if table == "moz_cookies" {
        let map_fn = map_firefox_cookie_row;
        if let Some(ref p) = param {
            let mut rows = stmt
                .query_map([p.as_str()], map_fn)
                .map_err(|e| format!("Query failed: {}", e))?;
            while let Some(Ok(c)) = rows.next() {
                cookies.push(c);
            }
        } else {
            let mut rows = stmt
                .query_map([], map_fn)
                .map_err(|e| format!("Query failed: {}", e))?;
            while let Some(Ok(c)) = rows.next() {
                cookies.push(c);
            }
        }
    } else {
        let map_fn = map_chrome_cookie_row;
        if let Some(ref p) = param {
            let mut rows = stmt
                .query_map([p.as_str()], map_fn)
                .map_err(|e| format!("Query failed: {}", e))?;
            while let Some(Ok(c)) = rows.next() {
                cookies.push(c);
            }
        } else {
            let mut rows = stmt
                .query_map([], map_fn)
                .map_err(|e| format!("Query failed: {}", e))?;
            while let Some(Ok(c)) = rows.next() {
                cookies.push(c);
            }
        }
    }

    Ok(cookies)
}

fn decrypt_cookie_value(encrypted: &[u8]) -> String {
    // Chrome v10+ 加密格式: "v10" + nonce (12 bytes) + ciphertext
    if encrypted.len() < 15 {
        return String::from_utf8_lossy(encrypted).to_string();
    }

    // 检查是否是 v10/v11 格式
    if &encrypted[0..3] == b"v10" || &encrypted[0..3] == b"v11" {
        #[cfg(target_os = "windows")]
        {
            return decrypt_windows(&encrypted[3..]);
        }

        #[cfg(target_os = "macos")]
        {
            return decrypt_macos(&encrypted[3..]);
        }

        #[cfg(target_os = "linux")]
        {
            return decrypt_linux(&encrypted[3..]);
        }
    }

    // 旧版本或未加密
    String::from_utf8_lossy(encrypted).to_string()
}

#[cfg(target_os = "windows")]
fn decrypt_windows(encrypted_data: &[u8]) -> String {
    String::from_utf8_lossy(&decrypt_windows_raw(encrypted_data)).to_string()
}

/// DPAPI decrypt bytes directly, preserving raw binary output.
/// Used by Chrome cookie encryption where the decrypted key is raw bytes, not text.
#[cfg(target_os = "windows")]
fn decrypt_windows_raw(encrypted_data: &[u8]) -> Vec<u8> {
    use windows::Win32::Security::Cryptography::{
        CryptUnprotectData, CRYPTPROTECT_UI_FORBIDDEN, CRYPT_INTEGER_BLOB,
    };

    unsafe {
        let data_in = CRYPT_INTEGER_BLOB {
            cbData: encrypted_data.len() as u32,
            pbData: encrypted_data.as_ptr() as *mut u8,
        };

        let mut data_out = CRYPT_INTEGER_BLOB {
            cbData: 0,
            pbData: std::ptr::null_mut(),
        };

        match CryptUnprotectData(
            &data_in,
            None,
            None,
            None,
            None,
            CRYPTPROTECT_UI_FORBIDDEN,
            &mut data_out as *mut _,
        ) {
            Ok(()) => {
                if data_out.pbData.is_null() || data_out.cbData == 0 {
                    return encrypted_data.to_vec();
                }
                std::slice::from_raw_parts(data_out.pbData, data_out.cbData as usize).to_vec()
            }
            Err(_) => encrypted_data.to_vec(),
        }
    }
}

#[cfg(target_os = "macos")]
fn decrypt_macos(encrypted_data: &[u8]) -> String {
    match get_chrome_safe_storage_password() {
        Some(password) => decrypt_chrome_aes_cbc(encrypted_data, &password),
        None => "[Failed to get encryption key from Keychain]".to_string(),
    }
}

#[cfg(target_os = "linux")]
fn decrypt_linux(encrypted_data: &[u8]) -> String {
    match get_chrome_safe_storage_password() {
        Some(password) => decrypt_chrome_aes_cbc(encrypted_data, &password),
        None => "[Failed to get encryption key from keyring]".to_string(),
    }
}

// AES-128-CBC 解密（Chrome macOS/Linux 使用 PBKDF2-SHA1 + AES-128-CBC）
#[cfg(any(target_os = "macos", target_os = "linux"))]
fn decrypt_chrome_aes_cbc(encrypted_data: &[u8], password: &str) -> String {
    use aes::cipher::BlockDecryptMut;
    use aes::Aes128;
    use cbc::cipher::KeyIvInit;
    use pbkdf2::pbkdf2_hmac;
    use sha1::Sha1;

    let mut key = [0u8; 16];
    pbkdf2_hmac::<Sha1>(password.as_bytes(), b"saltysalt", 1003, &mut key);

    let iv = [0x20u8; 16];
    let cipher = cbc::Decryptor::<Aes128>::new(&key.into(), &iv.into());

    let mut buf = encrypted_data.to_vec();
    match cipher.decrypt_padded_mut::<cbc::cipher::block_padding::Pkcs7>(&mut buf) {
        Ok(pt) => String::from_utf8_lossy(pt).to_string(),
        Err(_) => "[Decryption failed]".to_string(),
    }
}

#[cfg(any(target_os = "macos", target_os = "linux"))]
fn get_chrome_safe_storage_password() -> Option<String> {
    #[cfg(target_os = "macos")]
    {
        use keyring::Entry;
        let entry = Entry::new("Chrome Safe Storage", "Chrome");
        if let Ok(entry) = entry {
            if let Ok(password) = entry.get_password() {
                return Some(password);
            }
        }
    }

    #[cfg(target_os = "linux")]
    {
        use keyring::Entry;
        let entry = Entry::new("Chrome Safe Storage", "Chrome");
        if let Ok(entry) = entry {
            if let Ok(password) = entry.get_password() {
                return Some(password);
            }
        }
    }

    #[cfg(target_os = "windows")]
    {
        if let Ok(localappdata) = std::env::var("LOCALAPPDATA") {
            let local_state_path =
                PathBuf::from(localappdata).join("Google\\Chrome\\User Data\\Local State");

            if let Ok(content) = std::fs::read_to_string(&local_state_path) {
                if let Ok(json) = serde_json::from_str::<serde_json::Value>(&content) {
                    if let Some(encoded_key) = json["os_crypt"]["encrypted_key"].as_str() {
                        if let Ok(decoded) = general_purpose::STANDARD.decode(encoded_key) {
                            if decoded.len() >= 5 && &decoded[0..5] == b"DPAPI" {
                                let raw_key = decrypt_windows_raw(&decoded[5..]);
                                // Chrome AES key is 32 raw bytes; wrap via lossy for PBKDF2
                                return Some(String::from_utf8_lossy(&raw_key).into_owned());
                            }
                        }
                    }
                }
            }
        }
    }

    None
}
