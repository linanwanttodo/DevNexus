    #[test]
    fn test_export_as_netscape_format() {
        let cookies = vec![
            CookieEntry {
                name: "token".to_string(),
                value: "secret".to_string(),
                domain: ".example.com".to_string(),
                path: "/".to_string(),
                expires: 2000000000,
                secure: true,
                httponly: true,
            },
            CookieEntry {
                name: "non_secure".to_string(),
                value: "val".to_string(),
                domain: ".test.com".to_string(),
                path: "/app".to_string(),
                expires: 0,
                secure: false,
                httponly: false,
            },
        ];

        let mut output = String::from("# Netscape HTTP Cookie File\n");
        output.push_str("# https://curl.haxx.se/docs/http-cookies.html\n");
        output.push_str("# This file was generated by DevNexus\n\n");

        for cookie in &cookies {
            let expires = if cookie.expires > 0 {
                cookie.expires.to_string()
            } else {
                "0".to_string()
            };
            output.push_str(&format!(
                "{}\t{}\t{}\t{}\t{}\t{}\t{}\n",
                cookie.domain,
                if cookie.httponly { "TRUE" } else { "FALSE" },
                cookie.path,
                if cookie.secure { "TRUE" } else { "FALSE" },
                expires,
                cookie.name,
                cookie.value
            ));
        }

        assert!(output.starts_with("# Netscape"));
        assert!(output.contains(".example.com\tTRUE\t/\tTRUE\t2000000000\ttoken\tsecret"));
        assert!(output.contains(".test.com\tFALSE\t/app\tFALSE\t0\tnon_secure\tval"));
    }

