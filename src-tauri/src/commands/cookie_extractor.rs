fn get_firefox_cookie_path() -> Option<PathBuf> {
    #[cfg(target_os = "macos")]
    {
        if let Ok(home) = std::env::var("HOME") {
            let profiles_path = PathBuf::from(home)
                .join("Library/Application Support/Firefox/Profiles");
            if profiles_path.exists() {
                if let Ok(entries) = std::fs::read_dir(&profiles_path) {
                    for entry in entries.flatten() {
                        let cookie_path = entry.path().join("cookies.sqlite");
                        if cookie_path.exists() {
                            return Some(cookie_path);
                        }
                    }
                }
            }
        }
    }

    #[cfg(target_os = "linux")]
    {
        if let Ok(home) = std::env::var("HOME") {
            let profiles_path = PathBuf::from(home)
                .join(".mozilla/firefox");
            if profiles_path.exists() {
                if let Ok(entries) = std::fs::read_dir(&profiles_path) {
                    for entry in entries.flatten() {
                        let cookie_path = entry.path().join("cookies.sqlite");
                        if cookie_path.exists() {
                            return Some(cookie_path);
                        }
                    }
                }
            }
        }
    }

    #[cfg(target_os = "windows")]
    {
        if let Ok(appdata) = std::env::var("APPDATA") {
            let profiles_path = PathBuf::from(appdata)
                .join("Mozilla\\Firefox\\Profiles");
            if profiles_path.exists() {
                if let Ok(entries) = std::fs::read_dir(&profiles_path) {
                    for entry in entries.flatten() {
                        let cookie_path = entry.path().join("cookies.sqlite");
                        if cookie_path.exists() {
                            return Some(cookie_path);
                        }
                    }
                }
            }
        }
    }

    None
}

// ==================== Cookie 读取和解密 ====================

fn count_cookies(path: &PathBuf) -> Result<usize, String> {
    let conn = Connection::open(path).map_err(|e| format!("Failed to open database: {}", e))?;
    
    let count: i64 = conn.query_row(
        "SELECT COUNT(*) FROM cookies",
        [],
        |row| row.get(0)
    ).map_err(|e| format!("Query failed: {}", e))?;

    Ok(count as usize)
}

fn read_cookies(path: &PathBuf, domain_filter: Option<String>) -> Result<Vec<CookieEntry>, String> {
    let conn = Connection::open(path).map_err(|e| format!("Failed to open database: {}", e))?;
    
    let query = if let Some(domain) = domain_filter {
        format!("SELECT name, encrypted_value, host_key, path, expires_utc, is_secure, is_httponly FROM cookies WHERE host_key LIKE '%{}%'", domain)
    } else {
        "SELECT name, encrypted_value, host_key, path, expires_utc, is_secure, is_httponly FROM cookies".to_string()
    };

    let mut stmt = conn.prepare(&query).map_err(|e| format!("Prepare failed: {}", e))?;
    
    let cookie_iter = stmt.query_map([], |row| {
        let encrypted_value: Vec<u8> = row.get(1)?;
        Ok(CookieEntry {
            name: row.get(0)?,
            value: decrypt_cookie_value(&encrypted_value),
            domain: row.get(2)?,
            path: row.get(3)?,
            expires: row.get(4)?,
            secure: row.get(5)?,
            httponly: row.get(6)?,
        })
    }).map_err(|e| format!("Query failed: {}", e))?;

    let mut cookies = Vec::new();
    for cookie in cookie_iter {
        if let Ok(c) = cookie {
            cookies.push(c);
        }
    }

    Ok(cookies)
}

fn decrypt_cookie_value(encrypted: &[u8]) -> String {
    // Chrome v10+ 加密格式: "v10" + nonce (12 bytes) + ciphertext
    if encrypted.len() < 15 {
        return String::from_utf8_lossy(encrypted).to_string();
    }
    
    // 检查是否是 v10/v11 格式
    if &encrypted[0..3] == b"v10" || &encrypted[0..3] == b"v11" {
        #[cfg(target_os = "windows")]
        {
            return decrypt_windows(&encrypted[3..]);
        }
        
        #[cfg(target_os = "macos")]
        {
            return decrypt_macos(&encrypted[3..]);
        }
        
        #[cfg(target_os = "linux")]
        {
            return decrypt_linux(&encrypted[3..]);
        }
    }
    
    // 旧版本或未加密
    String::from_utf8_lossy(encrypted).to_string()
}

#[cfg(target_os = "windows")]
fn decrypt_windows(encrypted_data: &[u8]) -> String {
    use windows::Win32::Security::Cryptography::{
        CryptUnprotectData, CRYPTPROTECT_UI_FORBIDDEN,
    };
    use windows::Win32::Foundation::DATA_BLOB;

    // RAII guard: 确保 DPAPI 分配的内存一定被释放
    struct DataBlobGuard {
        ptr: *mut u8,
        len: u32,
    }

    impl DataBlobGuard {
        unsafe fn from_blob(blob: &DATA_BLOB) -> Option<Self> {
            if blob.pbData.is_null() {
                None
            } else {
                Some(Self {
                    ptr: blob.pbData,
                    len: blob.cbData,
                })
            }
        }

        fn as_slice(&self) -> &[u8] {
            unsafe { std::slice::from_raw_parts(self.ptr, self.len as usize) }
        }
    }

    impl Drop for DataBlobGuard {
        fn drop(&mut self) {
            unsafe {
                windows::Win32::System::Memory::LocalFree(self.ptr as _);
            }
        }
    }

    // SAFETY: DATA_BLOB 由 DPAPI 分配，DataBlobGuard 确保释放
    unsafe {
        let data_in = DATA_BLOB {
            cbData: encrypted_data.len() as u32,
            pbData: encrypted_data.as_ptr() as *mut _,
        };

        let mut data_out = DATA_BLOB {
            cbData: 0,
            pbData: std::ptr::null_mut(),
        };

        match CryptUnprotectData(
            &data_in,
            None,
            None,
            None,
            None,
            CRYPTPROTECT_UI_FORBIDDEN,
            Some(&mut data_out),
        ) {
            Ok(()) => {
                if let Some(guard) = DataBlobGuard::from_blob(&data_out) {
                    String::from_utf8_lossy(guard.as_slice()).to_string()
                } else {
                    String::from_utf8_lossy(encrypted_data).to_string()
                }
            }
            Err(_) => String::from_utf8_lossy(encrypted_data).to_string(),
        }
    }
}

#[cfg(target_os = "macos")]
fn decrypt_macos(encrypted_data: &[u8]) -> String {
    // macOS Chrome 使用 Keychain 存储加密密钥
    // 密钥存储在: ~/Library/Application Support/Google/Chrome/Local State
    let key = get_chrome_encryption_key();
    
    match key {
        Some(encryption_key) => decrypt_aes_gcm(encrypted_data, &encryption_key),
        None => "[Failed to get encryption key from Keychain]".to_string(),
    }
}

#[cfg(target_os = "linux")]
fn decrypt_linux(encrypted_data: &[u8]) -> String {
    // Linux Chrome 使用 GNOME Keyring 或 KWallet
    let key = get_chrome_encryption_key();
    
    match key {
        Some(encryption_key) => decrypt_aes_gcm(encrypted_data, &encryption_key),
        None => "[Failed to get encryption key from keyring]".to_string(),
    }
}

// AES-GCM 解密通用函数
fn decrypt_aes_gcm(encrypted_data: &[u8], key: &[u8; 32]) -> String {
    if encrypted_data.len() < 12 {
        return "[Invalid encrypted data]".to_string();
    }
    
    let (nonce_bytes, ciphertext) = encrypted_data.split_at(12);
    let nonce = Nonce::from_slice(nonce_bytes);
    
    let cipher = Aes256Gcm::new_from_slice(key).unwrap();
    
    match cipher.decrypt(nonce, ciphertext) {
        Ok(plaintext_vec) => String::from_utf8_lossy(&plaintext_vec).to_string(),
        Err(_) => "[Decryption failed]".to_string(),
    }
}

// 获取 Chrome 加密密钥（从 Local State 文件）
fn get_chrome_encryption_key() -> Option<[u8; 32]> {
    #[cfg(target_os = "macos")]
    {
        use keyring::Entry;
        
        // 尝试从 Keychain 获取
        let entry = Entry::new("Chrome Safe Storage", "Chrome");
        if let Ok(entry) = entry {
            if let Ok(password) = entry.get_password() {
                // Chrome 使用 PBKDF2 派生密钥
                let mut key = [0u8; 32];
                for (i, byte) in password.bytes().take(32).enumerate() {
                    key[i] = byte;
                }
                return Some(key);
            }
        }
        
        // 或者从 Local State 文件读取
        if let Ok(home) = std::env::var("HOME") {
            let local_state_path = PathBuf::from(home)
                .join("Library/Application Support/Google/Chrome/Local State");
            
            if let Ok(content) = std::fs::read_to_string(&local_state_path) {
                if let Ok(json) = serde_json::from_str::<serde_json::Value>(&content) {
                    if let Some(encoded_key) = json["os_crypt"]["encrypted_key"].as_str() {
                        if let Ok(decoded) = general_purpose::STANDARD.decode(encoded_key) {
                            if decoded.len() >= 5 && &decoded[0..5] == b"DPAPI" {
                                // DPAPI 加密的密钥，需要进一步解密
                                // 简化处理：返回前32字节
                                let mut key = [0u8; 32];
                                let len = std::cmp::min(32, decoded.len() - 5);
                                key[..len].copy_from_slice(&decoded[5..5+len]);
                                return Some(key);
                            }
                        }
                    }
                }
            }
        }
    }
    
    #[cfg(target_os = "linux")]
    {
        use keyring::Entry;
        
        // 尝试从 GNOME Keyring 获取
        let entry = Entry::new("Chrome Safe Storage", "Chrome");
        if let Ok(entry) = entry {
            if let Ok(password) = entry.get_password() {
                let mut key = [0u8; 32];
                for (i, byte) in password.bytes().take(32).enumerate() {
                    key[i] = byte;
                }
                return Some(key);
            }
        }
        
        // 或者从 Local State 文件
        if let Ok(home) = std::env::var("HOME") {
            let local_state_path = PathBuf::from(home)
                .join(".config/google-chrome/Local State");
            
            if let Ok(content) = std::fs::read_to_string(&local_state_path) {
                if let Ok(json) = serde_json::from_str::<serde_json::Value>(&content) {
                    if let Some(encoded_key) = json["os_crypt"]["encrypted_key"].as_str() {
                        if let Ok(decoded) = general_purpose::STANDARD.decode(encoded_key) {
                            if decoded.len() >= 5 && &decoded[0..5] == b"DPAPI" {
                                let mut key = [0u8; 32];
                                let len = std::cmp::min(32, decoded.len() - 5);
                                key[..len].copy_from_slice(&decoded[5..5+len]);
                                return Some(key);
                            }
                        }
                    }
                }
            }
        }
    }
    
    #[cfg(target_os = "windows")]
    {
        // Windows 从 Local State 读取并使用 DPAPI 解密
        if let Ok(localappdata) = std::env::var("LOCALAPPDATA") {
            let local_state_path = PathBuf::from(localappdata)
                .join("Google\\Chrome\\User Data\\Local State");
            
            if let Ok(content) = std::fs::read_to_string(&local_state_path) {
                if let Ok(json) = serde_json::from_str::<serde_json::Value>(&content) {
                    if let Some(encoded_key) = json["os_crypt"]["encrypted_key"].as_str() {
                        if let Ok(decoded) = general_purpose::STANDARD.decode(encoded_key) {
                            if decoded.len() >= 5 && &decoded[0..5] == b"DPAPI" {
                                // 使用 DPAPI 解密
                                return decrypt_windows(&decoded[5..]).bytes()
                                    .take(32)
                                    .enumerate()
                                    .fold([0u8; 32], |mut key, (i, b)| {
                                        key[i] = b;
                                        key
                                    }).into();
                            }
                        }
                    }
                }
            }
        }
    }
    
    None
}
