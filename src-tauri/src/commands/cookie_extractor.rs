#[derive(Serialize, Deserialize, Clone)]
pub struct CookieEntry {
    pub name: String,
    pub value: String,
    pub domain: String,
    pub path: String,
    pub expires: i64,
    pub secure: bool,
    pub httponly: bool,
}

#[derive(Serialize, Deserialize)]
pub struct BrowserInfo {
    pub name: String,
    pub cookie_path: String,
    pub cookie_count: usize,
}

/// 获取支持的浏览器列表
#[tauri::command]
pub fn get_supported_browsers() -> Vec<BrowserInfo> {
    let mut browsers = Vec::new();

    // Chrome
    if let Some(path) = get_chrome_cookie_path() {
        let count = count_cookies(&path).unwrap_or(0);
        browsers.push(BrowserInfo {
            name: "Chrome".to_string(),
            cookie_path: path.to_string_lossy().to_string(),
            cookie_count: count,
        });
    }

    // Edge
    if let Some(path) = get_edge_cookie_path() {
        let count = count_cookies(&path).unwrap_or(0);
        browsers.push(BrowserInfo {
            name: "Edge".to_string(),
            cookie_path: path.to_string_lossy().to_string(),
            cookie_count: count,
        });
    }

    // Firefox
    if let Some(path) = get_firefox_cookie_path() {
        let count = count_cookies(&path).unwrap_or(0);
        browsers.push(BrowserInfo {
            name: "Firefox".to_string(),
            cookie_path: path.to_string_lossy().to_string(),
            cookie_count: count,
        });
    }

    browsers
}

/// 提取指定浏览器的 Cookie
#[tauri::command]
pub fn extract_cookies(
    browser_name: String,
    domain_filter: Option<String>,
    max_results: Option<usize>,
) -> Result<Vec<CookieEntry>, String> {
    let cookie_path = match browser_name.as_str() {
        "Chrome" => get_chrome_cookie_path(),
        "Edge" => get_edge_cookie_path(),
        "Firefox" => get_firefox_cookie_path(),
        _ => return Err(format!("Unsupported browser: {}", browser_name)),
    };

    let path = cookie_path.ok_or_else(|| format!("{} cookie database not found", browser_name))?;

    if !path.exists() {
        return Err(format!("Cookie database does not exist: {:?}", path));
    }

    // 前端调用时 None → 默认 500 条限制，防止 IPC 数据过大导致崩溃
    let limit = max_results.unwrap_or(MAX_DISPLAY_COOKIES);
    let cookies = read_cookies(&path, domain_filter, Some(limit))?;
    Ok(cookies)
}

/// 导出为 Netscape Cookie 格式（curl/wget 兼容）
#[tauri::command]
pub fn export_as_netscape(
    browser_name: String,
    domain_filter: Option<String>,
) -> Result<String, String> {
    let path = get_browser_cookie_path(&browser_name)?;
    let cookies = read_cookies(&path, domain_filter, None)?;

    let mut output = String::from("# Netscape HTTP Cookie File\n");
    output.push_str("# https://curl.haxx.se/docs/http-cookies.html\n");
    output.push_str("# This file was generated by DevNexus\n\n");

    for cookie in cookies {
        let expires = if cookie.expires > 0 {
            cookie.expires.to_string()
        } else {
            "0".to_string()
        };

        output.push_str(&format!(
            "{}\t{}\t{}\t{}\t{}\t{}\t{}\n",
            cookie.domain,
            if cookie.httponly { "TRUE" } else { "FALSE" },
            cookie.path,
            if cookie.secure { "TRUE" } else { "FALSE" },
            expires,
            cookie.name,
            cookie.value
        ));
    }

    Ok(output)
}

/// 导出为 JSON 格式
#[tauri::command]
pub fn export_as_json(
    browser_name: String,
    domain_filter: Option<String>,
) -> Result<String, String> {
    let path = get_browser_cookie_path(&browser_name)?;
    let cookies = read_cookies(&path, domain_filter, None)?;
    serde_json::to_string_pretty(&cookies).map_err(|e| e.to_string())
}

// ==================== 浏览器 Cookie 路径检测 ====================

/// 根据浏览器名称获取 Cookie 数据库路径（供导出函数使用）
fn get_browser_cookie_path(browser_name: &str) -> Result<PathBuf, String> {
    let path = match browser_name {
        "Chrome" => get_chrome_cookie_path(),
        "Edge" => get_edge_cookie_path(),
        "Firefox" => get_firefox_cookie_path(),
        _ => return Err(format!("Unsupported browser: {}", browser_name)),
    };
    path.ok_or_else(|| format!("{} cookie database not found", browser_name))
}

fn get_chrome_cookie_path() -> Option<PathBuf> {
    #[cfg(target_os = "macos")]
    {
        if let Ok(home) = std::env::var("HOME") {
            let path = PathBuf::from(home)
                .join("Library/Application Support/Google/Chrome/Default/Cookies");
            if path.exists() {
                return Some(path);
            }
        }
    }

    #[cfg(target_os = "linux")]
    {
        if let Ok(home) = std::env::var("HOME") {
            let path = PathBuf::from(home).join(".config/google-chrome/Default/Cookies");
            if path.exists() {
                return Some(path);
            }
        }
    }

    #[cfg(target_os = "windows")]
    {
        if let Ok(localappdata) = std::env::var("LOCALAPPDATA") {
            let path = PathBuf::from(localappdata)
                .join("Google\\Chrome\\User Data\\Default\\Network\\Cookies");
            if path.exists() {
                return Some(path);
            }
        }
    }

    // 检查 Chromium
    if let Ok(ref home) = std::env::var("HOME") {
        let path = PathBuf::from(home).join(".config/chromium/Default/Cookies");
        if path.exists() {
            return Some(path);
        }
        // Snap 安装的 Chromium
        let snap_path =
            PathBuf::from(home).join("snap/chromium/current/.config/chromium/Default/Cookies");
        if snap_path.exists() {
            return Some(snap_path);
        }
    }

    None
}

